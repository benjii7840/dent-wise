import { useGetDoctors } from '@/hooks/use-doctors';
import { useState } from 'react';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/ui/card';
import { Edit, EditIcon, MailIcon, PhoneIcon, PlusIcon, StethoscopeIcon } from 'lucide-react';
import { Button } from '@/ui/button';
import Image from 'next/image';
import { Badge } from '@/ui/badge';
import AddDoctorDialog from './AddDoctorDialog';
import EditDoctorDialog from './EditDoctorDialog';
import { Doctor } from '@prisma/client';
import { se } from 'date-fns/locale';



function DoctorsManagement() {
  const { data: doctors = [] } = useGetDoctors();
  const [isAddDialogOpen, setIsAddDialogOpen] = useState(false);
  const [isEditDialogOpen, setIsEditDialogOpen] = useState(false);
  const [selectedDoctor, setSelectedDoctor] = useState<Doctor | null>(null);

  const handleEditDoctor = (doctor:Doctor) => {
    setSelectedDoctor(doctor);
    setIsEditDialogOpen(true);

  };
  const handleCloseEditDialog = () => {
    setIsEditDialogOpen(false);
    setSelectedDoctor(null);
  };

  return (
    <>
      <Card className="mb-12">
        <CardHeader className="flex justify-between items-center">
          <div>
            <CardTitle className="flex items-center gap-2">
              <StethoscopeIcon className="text-primary size-5" />
              Doctors Management
            </CardTitle>
            <CardDescription>
              Manage all registered doctors in the system.
            </CardDescription>
          </div>
          <Button
            onClick={() => setIsAddDialogOpen(true)}
            className="bg-gradient-to-r from-primary to-primary/80 hover:from-primary/90 hover:to-primary/100"
          >
            <PlusIcon className="mr-2 size-4" />
            Add New Doctor
          </Button>
        </CardHeader>

        <CardContent>
          {/* Doctors List Table */}
          <div className="space-y-4">
            {doctors.map((doctor) => (
              <div
                key={doctor.id}
                className="p-4 border border-border rounded-lg flex justify-between items-center"
              >
                <div className="flex items-center gap-4">
                  <Image
                    src={doctor.imageUrl || '/default-doctor.png'}
                    alt={doctor.name || 'Doctor'}
                    width={48}
                    height={48}
                    className="size-12 rounded-full object-cover ring-2 ring-background"
                  />
                  <div>
                    <div className="font-semibold">{doctor.name}</div>
                    <div className="text-sm text-muted-foreground">
                      {doctor.specialty || 'General Practitioner'}
                      <span className="ml-2 px-2 py-0.5 bg-muted rounded text-xs">
                        {doctor.gender === 'MALE' ? 'Male' : 'Female'}
                      </span>
                    </div>
                    <div className='flex items-center gap-4 mt-1'> 
                       <div className="flex items-center gap-1 text-xs text-muted-foreground">
                        <MailIcon className="h-3 w-3" />
                        {doctor.email}
                      </div>
                      <div className="flex items-center gap-1 text-xs text-muted-foreground">
                        <PhoneIcon className="h-3 w-3" />
                        {doctor.phone}
                      </div>
                    </div>
                  </div>
                </div>
                <div className='flex items-center gap-3'>
                  <div className='text-center'>
                    <div className='font-semibold text-primary'>
                      {doctor.appointmentsCount || 0}
                    </div>
                    <div className="text-xs text-muted-foreground">Appointments</div>
                  </div>
                  {doctor.isActive ? (
                    <Badge className="bg-green-100 text-green-800 hover:bg-green-100">Active</Badge>
                  ) : (
                    <Badge variant="secondary">Inactive</Badge>
                  )}
                   <Button
                    size="sm"
                    variant="outline"
                    className="h-8 px-3"
                    onClick={() => handleEditDoctor(doctor)}
                  >
                    <EditIcon className="size-4 mr-1" />
                    Edit
                  </Button>
                </div>
              </div>
            ))}
          </div>
        </CardContent>
      </Card>

      {/* Add Doctor Dialog */}
      <AddDoctorDialog isOpen={isAddDialogOpen} onClose={() => setIsAddDialogOpen(false)} />
      {/* Edit Doctor Dialog - To be implemented */}
     
      <EditDoctorDialog
        isOpen={isEditDialogOpen} 
        onClose={handleCloseEditDialog}
        doctor={selectedDoctor} 
        key={selectedDoctor?.id}
      />
    </>
  );
}

export default DoctorsManagement;
